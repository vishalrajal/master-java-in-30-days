class Variables {
    public static void main(String[] args) {

        int age = 21;
        double height = 5.1;
        char grade = 'A';
        boolean isStudent = true;
        System.out.println(age);
        System.out.println(height);
        System.out.println(grade);
        System.out.println(isStudent);


    }
    }